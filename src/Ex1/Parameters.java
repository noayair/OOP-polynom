package Ex1;

/**
 * This class is for help to get and set the parameters for the json function.
 */
public class Parameters {
    private int Width;
    private int Height;
    private double[] Range_X;
    private double[] Range_Y;
    private int Resolution;

    public int getWidth() {
        return Width;
    }

    public int getHeight() {
        return Height;
    }

    public double[] getRange_X() {
        return Range_X;
    }

    public double[] getRange_Y() {
        return Range_Y;
    }

    public int getResolution() {
        return Resolution;
    }
}
